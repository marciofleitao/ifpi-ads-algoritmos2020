# 10. Leia 2 números inteiros, calcule e escreva o quociente e o resto da divisão do 1o pelo 2o.
# ENTRADA
num1 = int(input('Introduza o primeiro número: '))
num2 = int (input('Introduza o segundo número: '))

# PROCESSAMENTO
quociente = num1 // num2
resto = num1 % num2

#SAÍDA
print(f'O valor do quociente é {quociente} e o valor do resto é {resto}.')