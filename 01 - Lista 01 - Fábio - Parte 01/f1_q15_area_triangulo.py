# 15. Leia o valor da base e altura de um triângulo, calcule e escreva sua área. (área=(base * altura)/2)
# ENTRADA
b = float(input('Insira o valor da base: '))
h = float(input('Insira o valor da altura: '))

# PROCESSAMENTO
area = (b*h)/2

# SAÍDA
print(f'O valor da área é igual a {area}.')