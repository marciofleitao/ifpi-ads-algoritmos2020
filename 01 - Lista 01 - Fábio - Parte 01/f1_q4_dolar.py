# 4. Leia o valor do dólar e um valor em dólar, calcule e escreva o equivalente em real (R$).
# ENTRADA

valor_do_dolar = 4.30
valor_em_dolar = float(input('Coloque a quantidade de dólar: '))

# PROCESSAMENTO 
valor_em_reais = valor_do_dolar*valor_em_dolar

# SAIDA
print(f'{valor_em_dolar} dolares equivale a {valor_em_reais} reais')
