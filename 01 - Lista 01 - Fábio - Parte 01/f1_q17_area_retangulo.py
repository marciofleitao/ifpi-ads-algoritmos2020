# 17. Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura)
# ENTRADA
b = float(input('Informe o valor da base: '))
h = float(input('Informe o valor da altura: '))

# PROCESSAMENTO
area = (b*h)/2

# SAÍDA
print(f'O valor da área é igual a {area}.')